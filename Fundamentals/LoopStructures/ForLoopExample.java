// Example of using the for loop in Java
public class ForLoopExample {
    public static void main(String[] args) {
        
        // Initializes the control variable; continuation condition; control variable update
        for(int i = 0; i < 5; i++) {
            // Code block executed on each iteration
            System.out.println("Value of i: " + i);
        }
    }
}
