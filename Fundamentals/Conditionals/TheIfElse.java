public class TheIfElse {
    public static void main(String[] args) {
        //define number to b e checked
        int number = 10;

        //IF -> checks if the condition (number > 0) is true
        if(number > 0){
            //true? => print message
            System.out.println("The number is greater than zero");
        } else {
            // the condition(number < 0) == false
            System.out.println("The number is less than zero");
        }
    }
}