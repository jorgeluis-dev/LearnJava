
public class PrimitiveVariables {
    public static void main(String[] args) {
        // Example int
        int isInt = 100;
        System.out.println("Integer value: " +isInt);

        //Example float
        float isFloat = 0.123456789f; 
        System.out.println("Float value: " +isFloat);

        //Example double
        double isDouble = 0.123456789;
        System.out.println("Double value: " +isDouble);

        // Example of boolean
        boolean isBoolean = true;
        System.out.println("Boolean value: " +isBoolean);

        // Example of character variable
        char isChar = 'A';
        System.out.println("Char value: " +isChar);
    }
}